/**
 * 把气象 16 方位（北北西、西北西…）收成 8 个主方向 +「向X」，
 * 普通人一眼能懂，不在 UI 里甩专业缩写。
 */
const PLAIN: Record<string, string> = {
  北: "向北",
  南: "向南",
  东: "向东",
  西: "向西",
  东北: "向东北",
  东南: "向东南",
  西南: "向西南",
  西北: "向西北",
  // 16 方位 → 8 方位
  北北东: "向东北",
  北东北: "向东北",
  东北偏北: "向东北",
  东东北: "向东北",
  东北东: "向东北",
  东东南: "向东南",
  东南东: "向东南",
  东南偏东: "向东南",
  南南东: "向东南",
  南东南: "向东南",
  南南西: "向西南",
  南西南: "向西南",
  西南偏南: "向西南",
  西西南: "向西南",
  西南西: "向西南",
  北北西: "向西北",
  北西北: "向西北",
  西北偏北: "向西北",
  西西北: "向西北",
  西北西: "向西北",
};

export function plainMoveDir(raw: string | null | undefined): string | null {
  if (!raw) return null;
  const t = raw.trim();
  if (PLAIN[t]) return PLAIN[t];
  // 兜底：含「西北/东北…」双字主方向
  for (const key of ["东北", "东南", "西南", "西北", "北", "南", "东", "西"]) {
    if (t.includes(key)) return PLAIN[key] ?? `向${key}`;
  }
  return `向${t}`;
}
